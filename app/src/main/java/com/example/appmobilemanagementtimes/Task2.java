package com.example.appmobilemanagementtimes;

public class Task2 {
    private String name;
    private String time;
    private boolean isDone;

    public Task2(String name, String time) {
        this.name = name;
        this.time = time;
        this.isDone= isDone;
    }

    public String getName() {
        return name;
    }

    public String getTime() {
        return time;
    }
    public boolean isDone() {
        return isDone;
    }

}
